#include "BaseSE.h"

using namespace CocosDenshion;
USING_NS_CC;

/*----------------------------------------------------------------------------------
 コンストラクタ
 ----------------------------------------------------------------------------------*/
BaseSE::BaseSE(const std::string& filePath):
mFilePath(filePath)
{
	
}

/*----------------------------------------------------------------------------------
 デストラクタ
 ----------------------------------------------------------------------------------*/
BaseSE::~BaseSE(){
	
}


/*----------------------------------------------------------------------------------
音量調整
 ----------------------------------------------------------------------------------*/
void BaseSE::setVolume(float volume){
	SimpleAudioEngine::getInstance() -> setEffectsVolume(volume);
}

/*----------------------------------------------------------------------------------
SE読み込み
 ----------------------------------------------------------------------------------*/
void BaseSE::preload(){
	SimpleAudioEngine::getInstance() -> preloadEffect(mFilePath.c_str());
}

/*----------------------------------------------------------------------------------
再生
 ----------------------------------------------------------------------------------*/
void BaseSE::play(bool loop, float pitch,float pan, float gain){
	SimpleAudioEngine::getInstance() -> playEffect(mFilePath.c_str(),loop,pitch,pan,gain);
}

/*----------------------------------------------------------------------------------
 停止
 ----------------------------------------------------------------------------------*/
void BaseSE::stop(unsigned int nId){
	SimpleAudioEngine::getInstance() -> stopEffect(nId);
}

/*----------------------------------------------------------------------------------
全て停止
 ----------------------------------------------------------------------------------*/
void BaseSE::stopAll(){
	SimpleAudioEngine::getInstance() -> stopAllEffects();
}

/*----------------------------------------------------------------------------------
 再度再生
 ----------------------------------------------------------------------------------*/
void BaseSE::resume(unsigned int nId){
	SimpleAudioEngine::getInstance() -> resumeEffect(nId);
}

/*----------------------------------------------------------------------------------
一時停止
 ----------------------------------------------------------------------------------*/

void BaseSE::pause(unsigned int nId){
	SimpleAudioEngine::getInstance() -> pauseEffect(nId);
}

/*----------------------------------------------------------------------------------
 全て一時停止
 ----------------------------------------------------------------------------------*/
void BaseSE::pauseAll(){
	SimpleAudioEngine::getInstance() ->pauseAllEffects();
}

/*----------------------------------------------------------------------------------
 解放
 ----------------------------------------------------------------------------------*/
void BaseSE::unload(){
	SimpleAudioEngine::getInstance() -> unloadEffect(mFilePath.c_str());
}

/*----------------------------------------------------------------------------------
 クリエイト
 ----------------------------------------------------------------------------------*/
BaseSE* BaseSE::create(const std::string& filePath){
	auto inst = new BaseSE(filePath);
	return inst;
}
